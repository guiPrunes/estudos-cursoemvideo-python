dg = (input('Digite algo, e veja suas características:'))
print('É alfanumérico?:', dg.isalnum())
print('É um número?:', dg.isnumeric())
print('É do alfabeto?:', dg.isalpha())
print('É maiúsculo?:', dg.isupper())
print('É minúsculo?', dg.islower())
print('É de qual tipo primitivo?:', type(dg))
print('Está capitalizada?:', dg.istitle())
