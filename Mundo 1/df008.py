# CONVERSOR DE METROS P/ CM E MM

print('Conversor de metros para centímetros e milímetros')
print('Abaixo, digite os valores a serem convertidos')
m = float(input('Digite um número (em metros):'))
print('Valores convertidos:')
print('Centímetros:{}cm'.format(m*100))
print('Milímetros:{}mm'.format(m*1000))
